  MergeSemantics buildNotificationOptionRow(String title, bool _isActive) {
    return MergeSemantics(
      child: ListTile(
        title: Text(
          title,
          style: TextStyle(
              fontSize: 18,
              fontWeight: FontWeight.w500,
              color: Colors.grey[600]),
        ),
        trailing: Switch(
          activeColor: PrimaryRed,
          value: _isActive,
          onChanged: (bool val) {
            setState(() {
              _isActive = val;
            });
          },
        ),
        onTap: () {
          setState(() {
            _isActive = !_isActive;
          });
        },
      ),
    );
